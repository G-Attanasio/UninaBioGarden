           EXISTS (SELECT 1 FROM raccolta WHERE fk_codattivita = NEW.fk_codattivita) THEN
            RAISE EXCEPTION 'Errore: l''attività è già registrata come irrigazione o raccolta.';
        END IF;

   
    ELSIF (TG_TABLE_NAME = 'irrigazione') THEN
        IF EXISTS (SELECT 1 FROM semina WHERE fk_codattivita = NEW.fk_codattivita) OR
           EXISTS (SELECT 1 FROM raccolta WHERE fk_codattivita = NEW.fk_codattivita) THEN
            RAISE EXCEPTION 'Errore: l''attività è già registrata come semina o raccolta.';
        END IF;

   
    ELSIF (TG_TABLE_NAME = 'raccolta') THEN
        IF EXISTS (SELECT 1 FROM semina WHERE fk_codattivita = NEW.fk_codattivita) OR
           EXISTS (SELECT 1 FROM irrigazione WHERE fk_codattivita = NEW.fk_codattivita) THEN
            RAISE EXCEPTION 'Errore: l''attività è già registrata come semina o irrigazione.';
        END IF;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE PLPGSQL;

