		WHERE
			CODATTIVITA = NEW.FK_CODATTIVITA
	);

SELECT
	C.NOME INTO NOME_COLTURA
FROM
	COLTURA C
WHERE
	C.CODCOLTURA = NEW.FK_CODCOLTURA;

SELECT
	A.DATAINIZIO INTO DATA_INIZIO_RACCOLTA
FROM
	ATTIVITA A
WHERE
	NEW.FK_CODATTIVITA = A.CODATTIVITA;

IF DATA_INIZIO_RACCOLTA < DATA_FINE_SEMINA THEN RAISE EXCEPTION 'Errore: la raccolta non può iniziare prima della fine della semina di ''%''.',
NOME_COLTURA;

