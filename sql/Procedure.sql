CREATE OR REPLACE PROCEDURE P_REGISTRA_ATTIVITA_SEMINA (
	V_DATATINIZIO DATE,
	V_DATAFINE DATE,
	V_METODOSEMINA TIPOSEMINA,
	V_QUANTITASEMI DECIMAL,
	V_COLTURA VARCHAR,
	V_PROGETTO INT,
	V_COLTIVATORE INT
) LANGUAGE PLPGSQL AS $$
DECLARE
V_CODATTIVITA INT;
BEGIN
INSERT INTO
	ATTIVITA (
	    STATOESECUZIONE,
		DATAINIZIO,
		DATAFINE,
		FK_COLTIVATORE,
		FK_PROGETTO
	)
VALUES
	(
		'PIANIFICATO',
		V_DATAINIZIO,
		V_DATAFINE,
		V_COLTIVATORE,
		V_PROGETTO
	)
RETURNING
	CODATTIVITA INTO V_CODATTIVITA;

INSERT INTO
	METODOSEMINA
VALUES
	(V_METODOSEMINA, V_CODATTIVITA);

INSERT INTO
	SEMINACOLTURA
VALUES
	(V_QUANTITASEMI, V_CODATTIVITA, V_COLTURA);

END;
$$;

