CREATE OR REPLACE PROCEDURE P_AVVIA_PROGETTO (V_CODPROGETTO INT) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE PROGETTOSTAGIONALE
SET
	STATOESECUZIONE = 'IN_CORSO'
WHERE
	CODPROGETTO = V_CODPROGETTO
	AND STATOESECUZIONE = 'PIANIFICATO';

END;
$$;

CREATE OR REPLACE PROCEDURE P_AVVIA_ATTIVITA (V_CODATTIVITA INT) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE ATTIVITA
SET
	STATOESECUZIONE = 'IN_CORSO'
WHERE
	CODATTIVITA = V_CODATTIVITA
	AND STATOESECUZIONE = 'PIANIFICATO';

END;
$$;

CREATE OR REPLACE PROCEDURE P_COMPLETA_PROGETTO (V_CODPROGETTO INT) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE PROGETTOSTAGIONALE
SET
	STATOESECUZIONE = 'COMPLETATO',
	DATAFINE = CURRENT_DATE
WHERE
	CODPROGETTO = V_CODPROGETTO
	AND STATOESECUZIONE = 'IN_CORSO';

END;
$$;

CREATE OR REPLACE PROCEDURE P_COMPLETA_ATTIVITA (V_CODATTIVITA INT) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE ATTIVITA
SET
	STATOESECUZIONE = 'COMPLETATO'
WHERE
	CODATTIVITA = V_CODATTIVITA
	AND STATOESECUZIONE = 'IN_CORSO';

END;
$$;

CREATE OR REPLACE PROCEDURE P_ANNULLA_PROGETTO (V_CODPROGETTO INT) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE PROGETTOSTAGIONALE
SET
	STATOESECUZIONE = 'ANNULLATO',
	DATAFINE = CURRENT_DATE
WHERE
	CODPROGETTO = V_CODPROGETTO
	AND STATOESECUZIONE IN ('PIANIFICATO', 'IN_CORSO');

UPDATE ATTIVITA
SET
	STATOESECUZIONE = 'ANNULLATO'
WHERE
	CODATTIVITA = V_CODATTIVITA
	AND STATOESECUZIONE IN ('PIANIFICATO', 'IN_CORSO');

END;
$$;

CREATE OR REPLACE PROCEDURE P_COMPLETA_RACCOLTA (V_CODATTIVITA INT, V_QUANTITAREALE DECIMAL) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE RACCOLTA
SET
	QUANTITAREALE = V_QUANTITAREALE
WHERE
	FK_CODATTIVITA = V_CODATTIVITA;

UPDATE ATTIVITA
SET
	STATOESECUZIONE = 'COMPLETATO'
WHERE
	V_CODATTIVITA = CODATTIVITA
	AND STATOESECUZIONE = 'IN_CORSO';

END;
$$;

CREATE OR REPLACE PROCEDURE P_NOTIFICA_VISUALIZZATA (V_CODNOTIFICA INT, V_IDUTENTE INT) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE NOTIFICADESTINATARIO
SET
	VISUALIZZATA = TRUE
WHERE
	FK_DESTINATARIO = V_IDUTENTE
	AND FK_CODNOTIFICA = V_CODNOTIFICA;

END;
$$;

CREATE OR REPLACE PROCEDURE P_DISATTIVA_LOTTO (V_CODLOTTO INT) LANGUAGE PLPGSQL AS $$
BEGIN
UPDATE LOTTOCOLTIVABILE
SET
	ATTIVO = FALSE
WHERE
	V_CODLOTTO = CODLOTTO;

END;
$$;

CREATE OR REPLACE PROCEDURE P_CANCELLA_PROGETTO (V_CODPROGETTO INT) LANGUAGE PLPGSQL AS $$
BEGIN
DELETE FROM PROGETTOSTAGIONALE
WHERE
	V_CODPROGETTO = CODPROGETTO;

END;
$$;

